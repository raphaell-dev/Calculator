function sum(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mult(a, b) {
    return a * b
}

function div(a,b) {
    return a / b;
}

let val_1 = 8;
let val_2 = 10;

console.log("Sum: " + sum(val_1, val_2));
console.log("Sub: " + sub(val_1, val_2));
console.log("Mult: " + mult(val_1, val_2));
console.log("Div: " + div(val_1, val_2));